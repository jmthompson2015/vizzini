    {
        return foods;
    }

    this.getRange = function(property)
    {
        var low = Number.MAX_VALUE;
        var high = Number.MIN_VALUE;

        var isLowSet = false;
        var isHighSet = false;

        foods.forEach(function(food)
        {
            var value = food[property];

            if (value < low)
            {
                low = value;
                isLowSet = true;
            }

            if (high < value)
            {
                high = value;
                isHighSet = true;
            }
        });

        if (!isLowSet)
        {
            low = 0.0;
        }

        if (!isHighSet)
        {
            high = 1.0;
        }

        var answer =
        {
            low: low,
            high: high
        };

        return answer;
    }

