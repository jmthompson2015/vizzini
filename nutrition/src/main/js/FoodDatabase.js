    this.getFoodByBrandName = function(brand, name)
    {
        var answer;

        for (var i = 0; i < foods.length; i++)
        {
            var food = foods[i];

            if (food.brand === brand && food.name === name)
            {
                answer = food;
                break;
            }
        }

        if (!answer)
        {
            LOGGER.warn("Didn't find food for brand, name = " + brand + ", " + name);
        }

        return answer;
    }

